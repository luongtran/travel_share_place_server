        function service() {
		$this->layout = false;
		$this->autoRender = false;
		Configure::write('debug', 0);
		ini_set("soap.wsdl_cache_enabled", "0"); // disabling WSDL cache
		$server = new SoapServer('http://localhost:8080/www/matching/cities/wsdl');
		$server->setClass("ID3"); 
		$server->handle();
	}
